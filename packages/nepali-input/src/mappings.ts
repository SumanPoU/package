/** Expected length for ASCII printable range 32–126 inclusive. */
export const NEPALI_MAP_SIZE = 95;

function assertMap(name: string, map: readonly string[]) {
  if (map.length !== NEPALI_MAP_SIZE) {
    throw new Error(
      `@itzsa/nepali-input: ${name} must have ${NEPALI_MAP_SIZE} entries (got ${map.length})`,
    );
  }
}

assertMap("unicodeMappings", unicodeMappings);
assertMap("preetiMappings", preetiMappings);
